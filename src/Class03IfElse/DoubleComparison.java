package Class03IfElse;

public class DoubleComparison {

	public static void main(String[] args) {
		/*Declare 2 double values and if value of 
		 * first variable is higher then the second,
		 *  print “Double value  is larger than  .” Otherwise print...
		 */

		double number1=37.21;
		double number2=37;
		
		if (number1>number2) {
			System.out.println("Double value "+number1 +" is larger than "+number2);
		} else {
			System.out.println("Double value "+number1+" is less than "+number2);
		}
	}
	
	

}
