package Class03IfElse;

public class RecapOperators {

	public static void main(String[] args) {
		
		/*
		 * Addition => +
		 * Subtraction => =
		 * Multiplication => *
		 * Division => /
		 * Modulus => %
		 * 
		 */
		
		int number1=11; //3+3+3 =2
		int number2=2;
		System.out.println("Answer = "+number1/number2);
		System.out.println("Remainder = "+ number1%number2);
		
		double number3=9;
		double number4=2;
		System.out.println(number3/number4);

		float number5=1;
		float number6=3;
		System.out.println(number5/number6); //dividing number5 by number6 
		
		double number7=1;
		double number8 = 3;
		System.out.println(number7/number8);
		
		
		
	}

}
